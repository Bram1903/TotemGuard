package com.deathmotion.totemguard.util;

import java.util.Map;

public class PlaceholderUtil {
    /**
     * Replaces placeholders in a command with their corresponding values.
     *
     * @param command      The command string containing placeholders.
     * @param placeholders A map of placeholder keys and their replacement values.
     * @return The command string with all placeholders replaced.
     */
    public static String replacePlaceholders(String command, Map<String, String> placeholders) {
        String result = command;
        for (Map.Entry<String, String> entry : placeholders.entrySet()) {
            result = result.replace(entry.getKey(), entry.getValue());
        }
        return result;
    }
}
