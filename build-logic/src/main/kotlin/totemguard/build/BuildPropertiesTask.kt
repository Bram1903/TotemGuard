package totemguard.build

import org.gradle.api.DefaultTask
import org.gradle.api.file.DirectoryProperty
import org.gradle.api.provider.Property
import org.gradle.api.tasks.Input
import org.gradle.api.tasks.Optional
import org.gradle.api.tasks.OutputDirectory
import org.gradle.api.tasks.TaskAction
import org.gradle.work.DisableCachingByDefault

@DisableCachingByDefault(because = "The generated resource embeds the current build timestamp.")
abstract class BuildPropertiesTask : DefaultTask() {

    companion object {
        const val TASK_NAME = "generateBuildProperties"
        const val RESOURCE_PATH = "META-INF/totemguard/build.properties"
    }

    @get:Input
    abstract val versionValue: Property<String>

    @get:Input
    @get:Optional
    abstract val gitCommit: Property<String>

    @get:OutputDirectory
    abstract val outputDir: DirectoryProperty

    @TaskAction
    fun generate() {
        val file = outputDir.file(RESOURCE_PATH).get().asFile
        file.parentFile.mkdirs()

        val version = versionValue.get()
        val fullCommit = gitCommit.orNull?.ifBlank { null } ?: ""
        val shortCommit = if (fullCommit.length >= GIT_SHORT_HASH_LENGTH) {
            fullCommit.take(GIT_SHORT_HASH_LENGTH)
        } else {
            fullCommit
        }
        val timestamp = System.currentTimeMillis()

        val body = buildString {
            append("# Generated by the TotemGuard build. Do not edit.\n")
            append("version=").append(escape(version)).append('\n')
            append("git.commit=").append(escape(shortCommit)).append('\n')
            append("git.commit.full=").append(escape(fullCommit)).append('\n')
            append("build.timestamp=").append(timestamp).append('\n')
        }
        file.writeText(body)
    }

    private fun escape(value: String): String {
        return value.replace("\\", "\\\\")
    }
}
